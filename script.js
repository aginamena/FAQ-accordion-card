
let question_one = document.querySelector("#question-one");
question_one.addEventListener("click", ()=>{
  question_one.style = "font-weight:700";
  let answer_one = document.querySelector(".answer-one");
  if (answer_one.style.display === "none") {
    answer_one.style.display = "block";
  } else {
    answer_one.style.display = "none";
  }
});

